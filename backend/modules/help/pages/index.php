<a name="main_bar"></a>
<h3>Barre principale</h3>
<p>
La barre principale est située en haut de l'écran.
Elle contient les éléments suivants :
</p>

<dl>
    <dt>Retour à l'accueil
    <dd>Un clic sur le titre situé sur gauche de la barre principale permet de revenir à la page d'accueil.
    <dt>Voir le site
    <dd>Ouvre le site administré dans une nouvelle fenetre
    <dt>Préférences
    <dd>Accès à la page des préférences, ou vous pouvez regler différents paramètres :
    <dd>
    - Nombre de résultats affichés par page<br>
    - Langue de l'interface<br>
    - Thème utilisé par l'interface, permet de changer son apparence<br>
    - Taille du texte, permet de reduire ou d'agrandir la taille de l'interface
    <dt>Aide
    <dd>Affichage de cette page
    <dt>Déconnexion
    <dd>Permet de se déconnecter de l'interface. Lors de la prochaine connexion un nom d'utilisateur et un mot de passe valide vous sera demandé.

</dl>


